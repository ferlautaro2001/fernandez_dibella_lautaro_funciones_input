# 2) # Mostrar 10 repeticiones de números descendentes desde el 10 al 1.

contador = 10  # Empezieza desde 10. (Contador en reversa)
while contador > 0:  # Condición para que baje hasta 1
    print(contador) # Imprime el contador por el que va la iteración.
    contador -= 1  # Descuenta de uno en 1 en 1 en cada vuelta.
print("Fin del ciclo") # El print al final para que no se repita en cada vuelta.